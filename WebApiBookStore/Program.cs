using Microsoft.AspNetCore.Hosting;
using Microsoft.Extensions.Configuration;
using Microsoft.Extensions.DependencyInjection;
using Microsoft.Extensions.Hosting;
using Microsoft.Extensions.Logging;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using WebApiBookStore.DBOperations;

namespace WebApiBookStore
{
    public class Program
    {
        public static void Main(string[] args)
        {
            var host = CreateHostBuilder(args).Build();
            using(var scope = host.Services.CreateScope()) //Uygulama ayaða kalktýðýndan initial datanýn in memory DB'ye yazýlmasý için Program.cs
                                                           //içerisinde configurasyon yapýlýyor
            {
                var services = scope.ServiceProvider;
                DataGenerator.Initialize(services); // yazdýðýmýz initialize fonksiyonu çaðýrýlýr
            }
             host.Run();
        }

        public static IHostBuilder CreateHostBuilder(string[] args) =>
            Host.CreateDefaultBuilder(args)
                .ConfigureWebHostDefaults(webBuilder =>
                {
                    webBuilder.UseStartup<Startup>();
                });
    }
}
